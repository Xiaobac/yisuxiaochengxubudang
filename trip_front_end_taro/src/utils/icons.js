/**
 * Get icon data URI with specified color.
 * Results are cached by name+color.
 */
export function getIconUri(name, color = '#333333') {
  const key = `${name}_${color}`;
  if (!iconCache[key]) {
    const path = ICON_PATHS[name];
    if (!path) {
      console.warn(`Icon "${name}" not found`);
      return '';
    }
    iconCache[key] = createIconUri(path, color);
  }
  return iconCache[key];
}
